part of 'iphone_13_mini_nine_bloc.dart';

/// Abstract class for all events that can be dispatched from the
///Iphone13MiniNine widget.
///
/// Events must be immutable and implement the [Equatable] interface.
class Iphone13MiniNineEvent extends Equatable {
  @override
  List<Object?> get props => [];
}

/// Event that is dispatched when the Iphone13MiniNine widget is first created.
class Iphone13MiniNineInitialEvent extends Iphone13MiniNineEvent {
  @override
  List<Object?> get props => [];
}
